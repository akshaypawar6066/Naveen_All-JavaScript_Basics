let day = "Friday";

switch (day) {

    case "Friday":
        {
            console.log("Its friday:")
            break;
        }

    case "Saturday":
        {
            console.log("Its saturday:")
            break;
        }

    case "Sunday":
        {
            console.log("Its Sunday:");
        }

    default:
        {
            console.log("Its default case:")
        }


}